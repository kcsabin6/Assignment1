package com.student8;

public class Student8 {
	String name;
	Integer age;
	String Address;
	
	public Student8() {
		super();
		
	}

	public Student8(String name, Integer age, String address) {
		super();
		this.name = name;
		this.age = age;
		Address = address;
	}

	@Override
	public String toString() {
		return "Student8 [name=" + name + ", age=" + age + ", Address=" + Address + "]";
	}




	
}
